import java.util.Scanner;

public class H4_11 {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in, "UTF-8");

		System.out
				.println("Pls input number (equal to raw and column of array) ");
		int a = input.nextInt();
		int array[][] = new int[a][a];
		int k = 1;
		for (int j = 0; j < a; j++) {
			for (int i = 0; i < a; i++) {
				array[i][j] = k;
				k++;
			}
		}
		for (int i = 0; i < a; i++) {
			for (int j = 0; j < a; j++) {
				System.out.print(array[i][j] + "  "); // kak se printira matrica
														// ako chislata sa ot
														// razlichen razrqd, da
														// sa edni pod drugi?
			}
			System.out.println();
		}
	}

}
